number1 = 15
number2 = 14

and_op = number1 & number2
print (f"{number1} & {number2} = {and_op}")

or_op = number1 | number2
print (f"{number1} |{number2} = {or_op}")

xor_op = number1 ^ number2 
print (f"{number1} ^ {number2} = {xor_op}")

not_op = ~number1
print (f"~{number1} = {xor_op}")

left_shift = number1 << 1
print (f"{number1} << 1 = {left_shift}")

right_shift = number1 >> 1
print (f"{number1} >> 1 = {right_shift}")